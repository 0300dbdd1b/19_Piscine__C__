int		ft_is_prime(int nb)
{
	int x;

	x = 13;
	if (nb <= 1)
		return (0);
	else if (nb == 2 || nb == 3 || nb == 5 || nb == 7 || nb == 11)
		return (1);
	else if (nb % 2 == 0 || nb % 3 == 0 || nb % 5 == 0 || nb % 7 == 0
			|| nb % 11 == 0)
		return (0);
	else
	{
		while (x < nb)
		{
			if (nb % x == 0)
				return (0);
			x = x + 2;
		}
		return (1);
	}
}
