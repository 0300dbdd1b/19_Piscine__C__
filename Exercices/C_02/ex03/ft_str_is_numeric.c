#include <unistd.h>

int		ft_str_is_numeric(char *str)
{
	int x;

	x = 0;
	if (str[x] == '\0')
		return (1);
	while (str[x] != '\0')
	{
		if (str[x] >= '0' && str[x] <= '9')
			x++;
		else
			return (0);
	}
	return (1);
}
