int		ft_str_is_printable(char *str)
{
	int x;

	x = 0;
	if (str[x] == '\0')
		return (1);
	while (str[x] != '\0')
	{
		if (str[x] >= 32)
			x++;
		else
			return (0);
	}
	return (1);
}
