void	ft_sort_int_tab(int *tab, int size)
{
	int x;
	int y;
	int z;

	x = 0;
	while (x < size)
	{
		y = 0;
		while (y < size - 1)
		{
			if (tab[y] > tab[y + 1])
			{
				z = tab[y];
				tab[y] = tab[y + 1];
				tab[y + 1] = z;
			}
			y++;
		}
		x++;
	}
}
